package com.kh.exam4;

public class Person {

	private String mane;
	private int age;
	
	
	public Person(String mane, int age) {
		super();
		this.mane = mane;
		this.age = age;
	}


	public String getMane() {
		return mane;
	}


	public void setMane(String mane) {
		this.mane = mane;
	}


	public int getAge() {
		return age;
	}


	public void setAge(int age) {
		this.age = age;
	}
	
	
	
}
